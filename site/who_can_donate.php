		<p><Strong>What organs can be donated by living donors? </Strong> <br /> <br />
		Living donor transplants are a viable alternative for patients in need of new organs. Many different types of organs can be supplied by living donors, including:</p>

		<ul>
			<li><Strong> Kidney </Strong></li>

			This is the most frequent type of living organ donation. All living kidney donors will experience a

			decrease in their kidney function, which varies depending on the donor’s age and medical history.

			<li><Strong> Liver </Strong> </li>

			Individuals can donate a segment of the liver, which has the ability to regenerate and regain full

			function.

			<li> <Strong> Lung </Strong> </li>

			Although lung lobes do not regenerate, individuals can donate a lobe of one lung.

			<li> <Strong> Intestine </Strong> </li>

			Although very rare, it is possible to donate a portion of your intestine.

			<li> <Strong> Pancreas </Strong> </li>

			Individuals can also donate a portion of the pancreas.

			<li><Strong>Heart </Strong> </li>

			A domino transplant makes some heart-lung recipients living heart donors. When a patient

			receives a heart-lung &quot;bloc&quot; from a deceased donor, his or her healthy heart may be given to an

			individual waiting for a heart transplant. Extremely rare, this procedure is used when physicians

			determine that the deceased donor lungs will function best if they are used in conjunction with the

			deceased donor heart.
		</ul>
