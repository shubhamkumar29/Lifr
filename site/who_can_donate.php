			<h1>Vascularized Composite Allografts (VCA)</h1>
<p>Vascularized Composite Allografts (VCAs) involve the transplantation of multiple structures that may include skin, bone, muscles, blood vessels, nerves and connective tissue. The most commonly known type of VCAs are for hand and face transplants.</p>


