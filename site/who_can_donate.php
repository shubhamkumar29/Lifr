			<h1>Who can Donate</h1>
<p>At the time of death the age and medical condition of the person will determine the organs and tissues that can be donated. People of all ages can be considered for organ donation. Very few medical conditions will prevent a person from being able to donate. For example, a person with severe asthma may not be able to donate lungs for transplantation but may be able to donate heart, liver, kidneys and eyes. It is important to understand that the very strict conditions that might prevent you from donating blood may not apply to organ donation and eye donation. There are instances when although you are not able to donate blood, heart valves and skin you can very well donate organs and eyes for transplantation. Even people with cancer can become eligible for donation. </p>


